/* Wap to calculate the area of the triangle,sqaure and rectangle */
class Areas
{
    public static void main(String args[])
    {
       int l=2,w=4,s=3,b=4,h=6,areat,areasq,arear;
       areat = (b*h)/2;
       areasq = s*s;
       arear = l*w;
       System.out.println("Area of triangle = " +areat);
       System.out.println("Area of square = " +areasq);
       System.out.println("Area of rectangle = " +arear);
    }
}